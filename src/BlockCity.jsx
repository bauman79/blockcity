function srand(s) { let n=s; return () => { n=(n*9301+49297)%233280; return n/233280; }; }
function genTerrain() {
  const rng = srand(77);
  const g = Array(MAP_H).fill(null).map(() => Array(MAP_W).fill(0));
  for (let ri=0; ri<2; ri++) {
    let x=ri===0?5+rng()*(MAP_W-10):0, y=ri===0?0:4+rng()*(MAP_H-8);
    let dx=ri===0?(rng()-.5)*.3:1, dy=ri===0?1:(rng()-.5)*.3;
    for (let i=0; i<90; i++) {
      const r=Math.round(y),c=Math.round(x);
      if(r>=0&&r<MAP_H&&c>=0&&c<MAP_W) g[r][c]=1;
      x+=dx+(rng()-.5)*.2; y+=dy+(rng()-.5)*.2;
      const m=Math.hypot(dx,dy)||1; dx/=m; dy/=m;
      if(x<0||x>=MAP_W||y<0||y>=MAP_H) break;
    }
  }
  [[2,2],[2,MAP_W-4],[MAP_H-4,2]].forEach(([cy,cx]) => {
    const r2=2+rng()*.8,cx2=cx+(rng()-.5)*2,cy2=cy+(rng()-.5)*2;
    for(let r=0;r<MAP_H;r++) for(let c=0;c<MAP_W;c++){
      if(g[r][c])continue;
      if(Math.hypot(c-cx2,r-cy2)<r2)g[r][c]=4;
    }
  });
  return g;
}
const TERRAIN = genTerrain();

