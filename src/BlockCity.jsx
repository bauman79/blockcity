const pickQs = (key, wordLogSnap={}) => {
  const pool = QB[key] || [];
  if(!pool.length) return [];
  // 약점 단어(틀린>맞힌) 우선, 없으면 순수 랜덤
  const weak = pool.filter(q => {
    const w = getWordFromQ(q[0]);
    if(!w) return false;
    const log = wordLogSnap[w];
    return log && log.w > log.c;
  });
  const rest = pool.filter(q => !weak.includes(q));
  const pick1 = weak.length ? [weak[Math.floor(Math.random()*weak.length)]] : [];
  const pick2 = [...rest].sort(()=>Math.random()-.5).slice(0, 2-pick1.length);
  const selected = [...pick1, ...pick2].slice(0, 2);
  return selected.map(([question, opts, ansIdx, example]) => {
    const correct = opts[ansIdx];
    const shuffledOpts = [...opts].sort(() => Math.random() - 0.5);
    const newAnsIdx = shuffledOpts.indexOf(correct);
    // [5]에 단어 추가 → QuizModal에서 정오답 기록에 사용
    return [question, shuffledOpts, newAnsIdx, example, getWordFromQ(question)];
  });
};

