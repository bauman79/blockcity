let _bgmNodes = [];
let _bgmGain = null;
let _bgmRunning = false;

const BGM = {
  start() {
    const ctx = getACtx(); if (!ctx || _bgmRunning) return;
    _bgmRunning = true;
    _bgmGain = ctx.createGain();
    _bgmGain.gain.setValueAtTime(0, ctx.currentTime);
    _bgmGain.gain.linearRampToValueAtTime(0.04, ctx.currentTime + 2);
    _bgmGain.connect(ctx.destination);

    const CHORDS = [
      [261.63, 329.63, 392.00],   // C
      [293.66, 369.99, 440.00],   // D minor
      [349.23, 440.00, 523.25],   // F
      [392.00, 493.88, 587.33],   // G
    ];
    const BEAT = 2.0; // 2초/화음
    let t = ctx.currentTime + 0.1;

    const scheduleChord = (chordIdx, time) => {
      const chord = CHORDS[chordIdx % CHORDS.length];
      chord.forEach((freq, i) => {
        const osc = ctx.createOscillator();
        const g   = ctx.createGain();
        osc.type = 'sine';
        osc.frequency.value = freq * (i === 0 ? 0.5 : 1); // 베이스 한 옥타브 아래
        g.gain.setValueAtTime(0, time);
        g.gain.linearRampToValueAtTime(0.3, time + 0.05);
        g.gain.linearRampToValueAtTime(0.0, time + BEAT * 0.95);
        osc.connect(g); g.connect(_bgmGain);
        osc.start(time); osc.stop(time + BEAT);
        _bgmNodes.push(osc);
      });
    };

    const scheduleLoop = () => {
      if (!_bgmRunning) return;
      for (let i = 0; i < 4; i++) scheduleChord(i, t + i * BEAT);
      t += BEAT * 4;
      setTimeout(scheduleLoop, BEAT * 3 * 1000);
    };
    scheduleLoop();
  },
  stop() {
    _bgmRunning = false;
    if (_bgmGain) { _bgmGain.gain.linearRampToValueAtTime(0, (_actx?.currentTime||0) + 0.5); }
    _bgmNodes.forEach(n => { try { n.stop((_actx?.currentTime||0)+0.6); } catch(e){} });
    _bgmNodes = [];
  },
  setVol(v) {
    if (_bgmGain && _actx) _bgmGain.gain.linearRampToValueAtTime(v * 0.04, _actx.currentTime + 0.3);
  }
};

