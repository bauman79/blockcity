function highlightWord(passage, word) {
  if (!word || !passage) return <span>{passage}</span>;
  const parts = passage.split(word);
  if (parts.length === 1) return <span>{passage}</span>;
  return (
    <span>
      {parts.map((part, i) => (
        <span key={i}>
          {part}
          {i < parts.length - 1 && (
            <mark style={{
              background:'linear-gradient(120deg,#fde68a 0%,#fbbf24 100%)',
              borderRadius:'3px', padding:'0 3px', fontWeight:800,
              color:'#78350f', boxShadow:'0 1px 3px rgba(0,0,0,.12)',
            }}>
              {word}
            </mark>
          )}
        </span>
      ))}
    </span>
  );
}

function extractTargetWord(qText) {
  const m = qText.match(/['''""]([^'''""]+)['''""]/) || qText.match(/'([^']+)'/);
  return m ? m[1] : null;
}

